defmodule ExPfds do
  @moduledoc """
  Elixir implementation of purely functional data structures as
  described in [_Purely Functional Data
  Structures_](https://www.amazon.com/Purely-Functional-Structures-Chris-Okasaki-ebook/dp/B00AKE1V04)
  by Chris Okasaki.

  Typespecs are included but are obviously not as rigorous as in the
  original ML code.  The interfaces to some functions have been
  altered to fit in with conventional Elixir (e.g., data structures
  are often the first argument to a function so that they can be use
  in a pipeline nicely).
  """
end
